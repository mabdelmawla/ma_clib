static _t_ma_u8 const gau8_ma_aes_cmn_rcon[] = 
{
	0x01, 0x02, 0x04, 0x08,
	0x10, 0x20, 0x40, 0x80,
	0x1b, 0x36
};

#define ROT_WORD(w)		MA_CMN_ROTR32(w,8)
#define RCON(i)			((_t_ma_u32)(gau8_ma_aes_cmn_rcon[i-1]<<24))	

static _t_ma_u8 _internal_aes_mod_mult(_t_ma_u8 a, _t_ma_u8 b){
	_t_ma_u16 tmp = a;
	_t_ma_u8 r = 0;
	while (b > 0){
		if (b & 0x1){
			r ^= (_t_ma_u8)tmp;
		}
		b >>= 1;
		tmp = (tmp & 0xff) << 1;
		if (tmp & 0x100){
			tmp ^= 0x1b;	// modular reduction by 0x1B
		}
	}
	return r;
}

_t_ma_u32 _internal_sub_word(_t_ma_u32 w_in){
	int i;
	_t_ma_u32 w = 0;
	for (i = 0; i < 4; i++){
		w |= gau8_ma_aes_cmn_s[MA_CMN_GET_BYTE(w_in, i)] << (i << 3);
	}
	return w;
}

/*
KeyExpansion(byte key[4*Nk], word w[Nb*(Nr+1)], Nk)
Nk	= 4 for 128-bit
= 6 for 192-bit
= 8 for 256-bit
Nb = 4
Nr = Nk + 6
*/
void ma_aes_cmn_key_expansion(_t_ma_u8 *p_key, _t_ma_u32 *p_w, _t_ma_u8 nk){
	/*
	word  temp
	i = 0
	while (i < Nk)
		w[i] = word(key[4*i], key[4*i+1], key[4*i+2], key[4*i+3])
		i = i+1
	end while
	*/
	_t_ma_u32 tmp;
	int i = 0;
	while (i < nk){
		p_w[i] = p_key[i * 4 + 3] | (p_key[i * 4 + 2] << 8) | (p_key[i * 4 + 1] << 16) | (p_key[i * 4 + 0] << 24);
		i++;
	}
	/*
	i = Nk
	while (i < Nb * (Nr+1)]
		temp = w[i-1]
		if (i mod Nk = 0)
			temp = SubWord(RotWord(temp)) xor Rcon[i/Nk]
		else if (Nk > 6 and i mod Nk = 4)
			temp = SubWord(temp)
		end if
		w[i] = w[i-Nk] xor temp
		i = i + 1
	end while
	*/
	i = nk;
	while (i < (4 * (1+MA_AES_CMN_GET_NR(nk)))){
		_t_ma_u32 rw, rcon;
		tmp = p_w[i - 1];
		if (0 == (i%nk)){
			rw = ROT_WORD(tmp);
			rcon = RCON(i / nk);
			tmp = _internal_sub_word(ROT_WORD(tmp)) ^ RCON(i / nk);
		}
		else if ((nk>6) && (4 == (i%nk))){
			tmp = _internal_sub_word(tmp);
		}
		p_w[i] = p_w[i - nk] ^ tmp;
		i++;
	}
}

void ma_aes_cmn_cov_swc_to_blk(_t_ma_u8 *blk, const _t_ma_u32 *swc){
	int i, j;
	for (j = 0; j < 4; j++){
		for (i = 0; i < 4; i++){
			blk[(3-i) * 4 + j] = (swc[j] >> (8 * i)) & 0xff;
		}
	}
}

void ma_aes_cmn_cov_u8_to_sb(const _t_ma_u8 *inp, _t_ma_aes_sb *sb){
	int i,j,n;
	for(i = 0, n = 0; i < 4; i++){
		for(j = 0; j < 4; j++){
			(*sb)[j][i] = inp[n];
			n++;
		}
	}
}

void ma_aes_cmn_cov_sb_to_u8(const _t_ma_aes_sb *sb, _t_ma_u8 *out){
	int i,j,n;
	for(i = 0, n = 0; i < 4; i++){
		for(j = 0; j < 4; j++){
			out[n] = (*sb)[j][i];
			n++;
		}
	}
}

void ma_aes_cmn_add_round_key(_t_ma_aes_sb* p_state, const _t_ma_u32 *w){
	int i;
	_t_ma_u8 a[16];
	ma_aes_cmn_cov_swc_to_blk(a, w);
	for(i = 0; i < 16; i++){
		(*p_state)[i/4][i%4] ^= a[i];
	}
}

void ma_aes_cmn_shift_rows(_t_ma_aes_sb* p_state){
	_t_ma_u32 *a = (_t_ma_u32 *)p_state;
	a++;
	*a = MA_CMN_ROTL32(*a, 8);
	a++;
	*a = MA_CMN_ROTL32(*a, 16);
	a++;
	*a = MA_CMN_ROTL32(*a, 24);
}

void ma_aes_cmn_mix_columns(_t_ma_aes_sb* p_state){
	int i;
	for (i = 0; i < 4; i++){
		_t_ma_u8 s[4] =
		{
			(*p_state)[0][i],
			(*p_state)[1][i],
			(*p_state)[2][i],
			(*p_state)[3][i]
		};
		(*p_state)[0][i] =
			_internal_aes_mod_mult(0x02, s[0]) ^
			_internal_aes_mod_mult(0x03, s[1]) ^
			s[2] ^
			s[3];
		(*p_state)[1][i] =
			s[0] ^
			_internal_aes_mod_mult(0x02, s[1]) ^
			_internal_aes_mod_mult(0x03, s[2]) ^
			s[3];
		(*p_state)[2][i] =
			s[0] ^
			s[1] ^
			_internal_aes_mod_mult(0x02, s[2]) ^
			_internal_aes_mod_mult(0x03, s[3]);
		(*p_state)[3][i] =
			_internal_aes_mod_mult(0x03, s[0]) ^
			s[1] ^
			s[2] ^
			_internal_aes_mod_mult(0x02, s[3]);
	}
}

void ma_aes_cmn_sub_bytes(_t_ma_aes_sb* p_state){
	int r, c;
	for(r = 0; r < 4; r++){
		for(c = 0; c < 4; c++)
			(*p_state)[r][c] = gau8_ma_aes_cmn_s[(*p_state)[r][c]];
	}
}

void ma_aes_cmn_inv_sub_bytes(_t_ma_aes_sb* p_state){
	int r, c;
	for(r = 0; r < 4; r++){
		for(c = 0; c < 4; c++)
			(*p_state)[r][c] = gau8_ma_aes_cmn_inv_s[(*p_state)[r][c]];
	}
}
